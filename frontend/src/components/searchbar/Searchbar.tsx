import React, {useState} from "react";
import {Whitespace} from "../whitespace/Whitespace";
import styles from "./Searchbar.module.css";
import { Typeahead } from 'react-bootstrap-typeahead';
import { InputGroup } from 'react-bootstrap';
import { Button } from 'react-bootstrap';

function Searchbar(props:any) {
    const [singleSelections, setSingleSelections] = useState(["Rensselaer, NY"]);
    let options = counties.map((county) => {
        return county.county + ", " + county.state;
    });

    const onChange = (selected: any) => {
        setSingleSelections(selected);
    }

    const handleSearch = () => {
        for (let i = 0; i < counties.length; i++) {
            if (counties[i].county + ", " + counties[i].state === singleSelections[0]) {
                props.onSearch(counties[i]);
                break;
            }
        }
    }


    return (
        <InputGroup id={styles["searchbar-container"]} className="px-lg-5 px-3 pb-lg-4">
            <Typeahead
                id={styles["search-location-selector"]}
                placeholder="Search Location"
                options={options}
                onChange={ onChange}
                selected={singleSelections}
            />
            <Button id={styles["search-location-btn"]} onClick={handleSearch}>Search</Button>
        </InputGroup>
    )
}

export {
    Searchbar,
    County
}

